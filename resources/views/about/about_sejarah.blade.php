  <h1>Sejarah</h1>
  <p>
    Sejarah dibentuknya Perkumpulan Menapak Indonesia bermula dari pemikiran
    dari beberapa aktifis pegiat lingkungan yang mana pada masa tahun 2000-an
    masih tergolong minim lembaga lokal yang memiliki kepedulian sosial pada
    daerah Kabupaten Berau. berdasarkan informasi pada tahun tersebut baru ada
    dua lembaga yang fokus melakukan pendampingan di Kabupaten Berau yaitu;
    Yayasan Kalbu dan Yayasan Berau Lestari yang mana dua lembaga tersebut
    berfokus pada isu kelautan dan pulau-pulau kecil dengan lokus area Kepulauan
    Derawan dan Kepulauan Maratua, sedangkan isu soal masyarakat sekitar hutan
    itu baru dilakukan oleh TNC sebagai lembaga internasional tentu memiliki
    banyak keterbatasan terutama soal intensitas pendampingan di masyarakat.
  </p>
  <p>
    Berdasarkan situasi tersebut maka kemudian Romi berinisiataif menyampaiakan
    gagasannya kepada kawan-kawan yang lain untuk membentuk sebuah lembaga yang
    memiliki bentuk perkumpulan agar kemudian dari lembaga ini diharapkan mampu
    mencetak kader-kader terbaik dari komunitas.
  </p>
  <p>
    Semasa proses persiapan pembentukan lembaga Perkumpulan Menapak Romi bersama
    Mustofa melakukan perjalanan ke wilayah Biduk-Biduk dan pada saat tiba di
    Biduk-Biduk mereka berdua melihat sebuah Gunung di hutan Kampung Teluk
    Sulaiman dan dengan rasa penasaran mereka bertanya kepada warga sekitar ada
    apa dibalik gunung tersebut. kemudian dari informasi warga sekitar terdapat
    sebuah komunitas adat basap yang masih bermukim di wilayah tersebut.
    Informasi ini cukup menarik bagi Romi dan Mustofa untuk lebih jauh mencari
    informasi sesungguhnya, siapa penghuni wilayah Teluk Sumbang dan apa
    aktivitas kesehariannya.
  </p>
