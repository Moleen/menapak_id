    perkumpulan dengan harapan komunitas yang di dampingi akan melahirkan kader
    kader kritis tingkat desa maka logo kaki menghadap ke bawah dengan filosofi
    kita yang harus mendahului untuk menjemput inisiatif pembelajaran di
    masyarakat.
  </p>
